#!/usr/bin/env python3
"""Format benchmark runtime/memory logs into comparison tables and plots.

Input expected: benchmark/results/performance/stage_performance.csv generated by
benchmark/run_benchmark.sh.

Outputs:
- CSV for latest run method comparison (runtime + peak memory)
- CSV aggregated across all runs (mean/std runtime + memory)
- PNG bar charts for latest run runtime and memory
"""

from __future__ import annotations

import argparse
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd

METHOD_TASKS = {
    "hmm": "model_export",
    "flare": "flare_inference",
    "rfmix": "rfmix_inference",
}
METHOD_ORDER = ["hmm", "flare", "rfmix"]
METHOD_COLORS = {
    "hmm": "#829eeb",
    "flare": "#93c4e0",
    "rfmix": "#9ce3e9",
}


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Format benchmark runtime and memory comparisons")
    parser.add_argument(
        "--input",
        default="benchmark/results/performance/stage_performance.csv",
        help="Input command-level performance CSV from run_benchmark.sh",
    )
    parser.add_argument(
        "--out-dir",
        default="benchmark/plots",
        help="Output directory for generated comparison files",
    )
    return parser


def build_latest_method_table(df: pd.DataFrame) -> pd.DataFrame:
    latest_run = df["run_id"].astype(str).max()
    latest_df = df[df["run_id"].astype(str) == latest_run].copy()

    rows: list[dict[str, float | str]] = []
    for method in METHOD_ORDER:
        task = METHOD_TASKS[method]
        method_rows = latest_df[latest_df["task"] == task]
        if method_rows.empty:
            continue
        row = method_rows.iloc[-1]
        max_rss_kb = pd.to_numeric(pd.Series([row["max_rss_kb"]]), errors="coerce").iloc[0]
        rows.append(
            {
                "run_id": str(latest_run),
                "method": method,
                "task": task,
                "runtime_seconds": float(row["elapsed_seconds"]),
                "peak_memory_kb": float(max_rss_kb) if pd.notna(max_rss_kb) else float("nan"),
                "peak_memory_gb": (float(max_rss_kb) / 1_048_576) if pd.notna(max_rss_kb) else float("nan"),
            }
        )

    return pd.DataFrame(rows)


def build_aggregate_method_table(df: pd.DataFrame) -> pd.DataFrame:
    method_rows = []
    for method, task in METHOD_TASKS.items():
        subset = df[df["task"] == task].copy()
        if subset.empty:
            continue

        subset["elapsed_seconds"] = pd.to_numeric(subset["elapsed_seconds"], errors="coerce")
        subset["max_rss_kb"] = pd.to_numeric(subset["max_rss_kb"], errors="coerce")

        method_rows.append(
            {
                "method": method,
                "runs": int(subset["run_id"].nunique()),
                "runtime_seconds_mean": float(subset["elapsed_seconds"].mean()),
                "runtime_seconds_std": float(subset["elapsed_seconds"].std()),
                "peak_memory_kb_mean": float(subset["max_rss_kb"].mean()),
                "peak_memory_kb_std": float(subset["max_rss_kb"].std()),
                "peak_memory_gb_mean": float(subset["max_rss_kb"].mean() / 1_048_576),
                "peak_memory_gb_std": float(subset["max_rss_kb"].std() / 1_048_576),
            }
        )

    out = pd.DataFrame(method_rows)
    if out.empty:
        return out
    return out.set_index("method").reindex(METHOD_ORDER).reset_index()


def plot_latest_runtime(latest_df: pd.DataFrame, out_path: Path) -> None:
    if latest_df.empty:
        return

    ordered = latest_df.set_index("method").reindex(METHOD_ORDER).dropna(subset=["runtime_seconds"]).reset_index()

    plt.figure(figsize=(7, 4.8))
    bars = plt.bar(
        ordered["method"],
        ordered["runtime_seconds"],
        color=[METHOD_COLORS.get(m, "#888888") for m in ordered["method"]],
    )
    plt.ylabel("Runtime (seconds)")
    plt.title("Method Runtime Comparison (Latest Run)")
    plt.grid(axis="y", alpha=0.25)

    for bar, value in zip(bars, ordered["runtime_seconds"]):
        plt.text(bar.get_x() + bar.get_width() / 2, bar.get_height(), f"{value:.2f}", ha="center", va="bottom")

    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    plt.close()


def plot_latest_memory(latest_df: pd.DataFrame, out_path: Path) -> None:
    if latest_df.empty:
        return

    ordered = latest_df.set_index("method").reindex(METHOD_ORDER).dropna(subset=["peak_memory_gb"]).reset_index()

    plt.figure(figsize=(7, 4.8))
    bars = plt.bar(
        ordered["method"],
        ordered["peak_memory_gb"],
        color=[METHOD_COLORS.get(m, "#888888") for m in ordered["method"]],
    )
    plt.ylabel("Peak Memory (GiB)")
    plt.title("Method Peak Memory Comparison (Latest Run)")
    plt.grid(axis="y", alpha=0.25)

    for bar, value in zip(bars, ordered["peak_memory_gb"]):
        plt.text(bar.get_x() + bar.get_width() / 2, bar.get_height(), f"{value:.2f}", ha="center", va="bottom")

    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    plt.close()


def main() -> None:
    args = build_parser().parse_args()

    input_path = Path(args.input)
    if not input_path.exists():
        raise FileNotFoundError(f"Performance input file not found: {input_path}")

    df = pd.read_csv(input_path)

    required = {"run_id", "task", "elapsed_seconds", "max_rss_kb"}
    missing = required.difference(df.columns)
    if missing:
        raise ValueError(f"Input file missing required columns: {sorted(missing)}")

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    latest_df = build_latest_method_table(df)
    aggregate_df = build_aggregate_method_table(df)

    latest_csv = out_dir / "benchmark_performance_latest.csv"
    aggregate_csv = out_dir / "benchmark_performance_summary.csv"
    latest_df.to_csv(latest_csv, index=False)
    aggregate_df.to_csv(aggregate_csv, index=False)

    runtime_plot = out_dir / "benchmark_runtime_comparison.png"
    memory_plot = out_dir / "benchmark_memory_comparison.png"

    plot_latest_runtime(latest_df, runtime_plot)
    plot_latest_memory(latest_df, memory_plot)

    print(f"Saved latest performance CSV: {latest_csv.resolve()}")
    print(f"Saved summary performance CSV: {aggregate_csv.resolve()}")
    if runtime_plot.exists():
        print(f"Saved plot: {runtime_plot.resolve()}")
    if memory_plot.exists():
        print(f"Saved plot: {memory_plot.resolve()}")


if __name__ == "__main__":
    main()
